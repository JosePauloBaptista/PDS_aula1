package Modelo;

public class Calculadora {

	public Float calcAdicao(Float n1, Float n2) {
		Float Adicao = n1 + n2;
		return Adicao;
	}

	public Float calcSubtracao(Float n1, Float n2) {
		Float Subtracao = n1 - n2;
		return Subtracao;
	}

	public Float calcMultiplicacao(Float n1, Float n2) {
		Float Multiplicacao = n1 * n2;
		return Multiplicacao;
	}

	public Float calcDivisao(Float n1, Float n2) {
		Float Divisao = n1 / n2;
		return Divisao;
	}

}
